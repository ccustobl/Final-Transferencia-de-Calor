TransistorArea = TransistorSide^2; %m2
qTotal = 210.5; %W - Total Power of each Transistor
transistorX1 = Length-TransistorSide/2-(Length-BoardSide)/2; %m - X center coordinate of Transistor 1
transistorY1 = BaseWidth-TransistorSide/2-(BaseWidth-BoardSide)/2; %m - Y center coordinate of Transistor 1
