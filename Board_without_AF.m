        else
            qFin = 0; % qFin = 0 to avoid rounding errors
            hc = hc_Base(Length,Tinf,Tij);
            qOut_Base = qOut_Base + hc*DX*DY*Tinf; %W - Heat Dissipated by the base of the HS
        end
        
