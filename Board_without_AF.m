        if sum(iNode==Percen)==1
            flag = flag + 10;
            disp(strcat(num2str(flag),'%'))
        end
        if flag>=90
            flag = 0;
        end
    end
    
    % Status Update
    disp('100%')
    disp('A & BC CALCULATED')
    
