%% Calculo del coeficiente de conveccion natural en la aleta
function h = hc_Fin(Largo,Tinf,Tsup)
% Parametros: Velocidad del aire, Largo del techo, Temperatura del
% ambiente, Temperatura del borde superior
TFilm = (Tinf + Tsup)/2;
[ ~, Beta_air, ~, k_aire, ~, nu_air, Pr_aire] = Properties_Air(TFilm);
 
% Opcion 1
% S = EspaciadoAleta;
% Ras = (9.81*Beta_Air*(Tsup-Tinf)*S)/(nu_air^2);
% Nu = ((1500/Ras)^2+(0.081*Ras^0.39)^-2)^-0.5
 
% Opcion 2
Gr = (9.81*Beta_air*(Tsup-Tinf)*Largo^3)/(nu_air^2);
Nu = 0.68 * Pr_aire^(1/2) * (Gr^(1/4)/(0.952+Pr_aire)^1/4); % EQ 5.12b
 
% Opcion 3 [Table 5.1] (No Aplica)
% Gr = (9.81*Beta_air*(Tsup-Tinf)*Largo^3)/(nu_air^2);
% Ra = Gr * Pr_aire;
% P = Ra/Largo^4;
% S = EspaciadoAleta;
% Nu = k_aire/S * (576/(P^2*S^3)+(2.873/(P^(1/2)*S^2)))^-(1/2);
 
h = Nu*k_aire/Largo;
end
 
%% Calculo del coeficiente de conveccion natural en la base
function h = hc_Base(Length,Tinf,Tsup)
% Parametros: Velocidad del aire, Largo del techo, Temperatura del
% ambiente, Temperatura del borde superior
TFilm = (Tinf + Tsup)/2;
[ ~, Beta_air, ~, k_aire, ~, nu_air, Pr_aire] = Properties_Air(TFilm);
Gr = (9.81*Beta_air*(Tsup-Tinf)*Length^3)/(nu_air^2);
Ra = Gr * Pr_aire;
if Ra<=10^7
    Nu = 0.54*Ra^0.25; % EQ 5.15
else
    Nu = 0.15*Ra^(1/3); % EQ 5.16
end
h = Nu*k_aire/Length;
end


