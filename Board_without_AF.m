%% Transistor Checker
function isTransistor = isTransistorBelow(vols, transistorX1, transistorX2, transistorY1, transistorY2, transistorSide)
xRange1 = false;
yRange1 = false;
xRange2 = false;
yRange2 = false;
isTransistor = false;

if (vols(1) > transistorX1 - transistorSide/2 && vols(1) < transistorX1 + transistorSide/2) 
    xRange1 = true;
elseif(vols(1) > transistorX2 - transistorSide/2 && vols(1) < transistorX2 + transistorSide/2) % Node is in the X1 or X2 range range
    xRange2 = true;
end

if (vols(2) > transistorY1 - transistorSide/2 && vols(2) < transistorY1 + transistorSide/2)
    yRange1 = true;
elseif (vols(2) > transistorY2 - transistorSide/2 && vols(2) < transistorY2 + transistorSide/2) % Node is in the Y1 or Y2 range
    yRange2 = true;
end

if ((yRange1 && xRange1) || (yRange2 && xRange2))
    isTransistor = true;
end

end

%% Fin Checker
function pos = isFinOnTop(Node, FinNumber, FinWidth, FinSpacing)
pos = false;
if Node(2) < FinWidth
    pos = true;
else
    for iFin = 1:FinNumber-1
        if Node(2) > iFin*(FinSpacing + FinWidth) && Node(2) < iFin*(FinSpacing + FinWidth) + FinWidth
            pos = true;
            break;
        end
    end
end
